print(x / y / z)  # Division
print(x // y // z)  # Floor Division
print(x % y % z)  # Modulus
print(x ** y ** z)  # Exponentiation
print(x + y * z)  # Mixed operations
print((x + y) * z)  # Parentheses to change precedence
print(x + (y * z))  # Parentheses to change precedence
